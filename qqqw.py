# Заданные параметры
M = 80  # масса блока в граммах
m1 = 400  # масса груза m1 в граммах
g = 9.81  # ускорение свободного падения в м/с^2 (можно оставить для физического контекста)

# Функция для расчета ускорения
def calculate_acceleration(M, m1, m2):
    return -(m1 - m2) * g / (m1 + m2 + M)

# Изменение массы груза m2 от 10 до 300 с шагом 20
for m2 in range(10, 301, 20):
    a = calculate_acceleration(M, m1, m2)
    print(f"Для m2 = {m2} г, ускорение a = {a:.2f} м/с²")
